package com.app.backend_app.security.service;

import org.springframework.security.core.userdetails.UserDetails;
import org.springframework.security.core.userdetails.UserDetailsService;
import org.springframework.security.core.userdetails.UsernameNotFoundException;
import org.springframework.stereotype.Service;

import com.app.backend_app.repository.UsuarioRepository;

import lombok.RequiredArgsConstructor;

//servicio para cargas a los usuarios desde la BD
@Service
@RequiredArgsConstructor
public class CustomUserDetailsService implements UserDetailsService {

    private final UsuarioRepository usuarioRepository;

    @Override
    public UserDetails loadUserByUsername(String email) throws UsernameNotFoundException {
        return usuarioRepository.findByEmail(email).orElseThrow(()-> new UsernameNotFoundException("Usuario no encontrado con el email: "+email));
    }

}
